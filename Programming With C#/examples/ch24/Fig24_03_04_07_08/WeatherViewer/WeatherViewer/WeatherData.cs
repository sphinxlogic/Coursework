// Class to store weather information for one day.
using System;

namespace WeatherViewer
{
    public class WeatherData
    {
        public string DayOfWeek { get; set; }
        public string WeatherImage { get; set; }
        public string HighFahrenheit { get; set; }
        public string LowFahrenheit { get; set; }
        public string HighCelsius { get; set; }
        public string LowCelsius { get; set; }
    } // end WeatherData
} // end WeatherViewer

