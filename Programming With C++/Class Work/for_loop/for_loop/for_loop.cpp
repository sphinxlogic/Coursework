// for_loop.cpp : Defines the entry point for the console application.
//

#include "stdafx.h"
#include <iostream>

using std::cout;
using std::endl;

int _tmain(int argc, _TCHAR* argv[])
{
	
	
	for (int c=1; c<=10; c++)
	{
		cout << c << endl;
	}
	
	system("pause");

	return 0;
}

